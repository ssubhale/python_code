
## 2.Comparison Operators-
'''
A comparison operator in python, also called python relational operator, It compares the values of two operands 
and returns True or False based on the some condition.
'''
'''
less than : <
less than or equal to : <=
greater than : >
greater than or equal to : >= 
equal to : ==
not equal to : !=
'''
a = 12
b = 10

print('a is less than b:',a < b)                #False
print('a is less than or equal to b:',a <= b)   #False
print('a is greater than b : ', a > b)          #True
print('a is greater than or equal to b:',a >= b)#True
print('a is equal to b:',a == b)                #False
print('a is not equal to b:',a != b)            #True
