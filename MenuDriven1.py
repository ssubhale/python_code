
#_seven arithmatical operators

def Add (a, b) :
	print('Addition : ', a + b)

def Sub (a, b) :
	print('Subtraction : ', a  - b)

def Mul (a, b) :
	print('Multiplication :', a * b)

def Div (a, b) :
	print('Division : ', a / b)

def FDiv(a, b) :
	print('Floor Division : ', a // b)

def Mod(a, b) :
	print('Modules : ', a % b)

def Exp(a, b) :
	print('Exponant : ', a ** b)

