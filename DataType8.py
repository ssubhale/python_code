## Dictionary
'''
Dictionary is used to store data in key:value pair, its mutable, insertion order is preserved from 3.7ver of python
keys are immutable and value can be mutable as well as immutable, if we try to duplicate key then updated key considered
dictionary notation is {}, key value pair separated by colon : , indexing and slicing is not allowed
'''
d = {1:"C", 2:"C++", 3:"Python",4:"Java", 5:"C#"}
print(d)
print(type(d))