## 1.Arithmatic Operators-
'''
An Arithmetic Operator is a mathematical function that performs a calculation on two operands.
'''
'''
1.Addition +
2.Subtraction -
3.Multiplication * 
4.Division / 
5.Floor division //
6.Modulus % 
7.Exponant **
'''
a = 12
b = 10

print('Addition:',a + b)        #22
print('Subtraction:',a - b)     #8
print('Multiplication;',a * b)  #105
print('Division:',a / b)        #2.14.... original value
print('Floor division:',a // b) #2 int value
print('Modulus:',a % b)         #1 reminder
print('Exponant:',a ** b)       #170859375 power